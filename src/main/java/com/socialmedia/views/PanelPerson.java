    private void btnAddFriendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFriendActionPerformed
        // TODO add your handling code here:
        String message = JOptionPane.showInputDialog(null, "Deseas agregar un mensaje a enviar con la solicitud?. En caso de que no solo dejalo vacio");
        
        if (message == null || message.isEmpty() || message.isBlank()) {
            message = "";
        }
        
        // Create the firend request between the logged user and the other profile
        FriendRequest friendReq = new FriendRequest(
                this.profileLogged.getIdProfile(), 
                this.profile.getIdProfile(), 
                false, 
                false, 
                new Date(), 
                message
        );
        
        if (Dao.saveFriendRequest(friendReq)) {
            JOptionPane.showMessageDialog(null, "Solicitud enviada correctamente");
            this.homeWindow.initPersonsData();
        } else {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al enviar la solicitud, intentalo mas tarde");
        }
    }//GEN-LAST:event_btnAddFriendActionPerformed


