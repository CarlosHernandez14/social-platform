    private void initData() {
        
        this.labelName.setText(this.profile.getName() + " " + this.profile.getLastName());
        
        String address = this.profile.getAddress();
        this.labelAddress.setText(address == null ? "" : "Vive en " + address);
        
        // Check if the profile is already a friend
        if (Dao.isFriend(this.profileLogged.getIdProfile().toString(), this.profile.getIdProfile().toString())) {
            this.btnAddFriend.setVisible(false);
            this.labelAddress.setText("Ya son amigos!");
        } else if (Dao.isFriendRequestRejected(this.profileLogged.getIdProfile().toString(), this.profile.getIdProfile().toString())) {
            this.btnAddFriend.setVisible(false);
            this.labelAddress.setText("Lo siento, esta persona  no quiere ser tu amigo :(");
        } else if (Dao.isFriendRequestSent(this.profileLogged.getIdProfile().toString(), this.profile.getIdProfile().toString())) {
            this.btnAddFriend.setVisible(false);
            this.labelAddress.setText("Solicitud de amistad enviada!");            
        } else if (Dao.isFriendRequestSent(this.profile.getIdProfile().toString(), this.profileLogged.getIdProfile().toString())) {
            this.btnAddFriend.setVisible(false);
            this.labelAddress.setText("Ya tienes una solicitud de esta persona, aceptala!");
        }
        
        // Get the profile image if exists
        if (this.profile.getProfileImageId() != null) {
            Image image = Dao.getImageById(this.profile.getProfileImageId().toString());
            
            File foto = new File("images/" + image.getImage_path());
            
            try {
                byte[] fotoBytes = Files.readAllBytes(foto.toPath());

                this.panelImageProfile.setIcon(new ImageIcon(fotoBytes));

                this.panelImageProfile.revalidate();
                this.panelImageProfile.repaint();

            } catch (IOException ex) {
                System.out.println("Error al obtener la imagen del arhivo");
            }
        }
        
    }

